CREATE view [dbo].[User_Data]
as
select Id as [UID],
	null as ClientID,
	FirstName as FName,
	LastName as SName,
	Username as UserName,
	[Password],
	[Admin] as ISAdmin,
	null as LastAuth,
	LastLoggedIn as LastLogin,
	null as ChangedPW,
	0 as CurrentApplication,
	'' as CurrentPage,
	null as GlobalLogin,
	null as [Hash],
	1 as [Enabled],
	case when Deleted=1 then 'Y'
	else 'N'
	end as Deleted,
	LoggedInFrom as IP,
	Locked as validator_lockedout,
	null as LastUnlocked,
	LogonAttempts as FailedLogons,
	Email,
	DDIPhone as Telephone,
	null as PasswordHistory,
	MobilePhone as Mobile,
	TFAStatus,
	null as TFAGenerated,
	null as TeamName,
	0 as KeepSignedIn,
	LastStatus as LastSecurityStatus,
	CreateDate,
	'sys' as CreatedBy,
	ModifiedOn as AlteredDate, -- FFS
	ModifiedBy as AlteredBy,	-- FFS
	null as GroupEmail
from [SSO-Accounts].dbo.[User]	-- DOUBLE FFS

GO
